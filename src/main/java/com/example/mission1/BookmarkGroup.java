package com.example.mission1;

public class BookmarkGroup {
    private int groupId;
    private String groupName;
    private int groupOrder;
    private String groupRegDate;
    private String groupEditDate;

    public int getGroupId() {
        return groupId;
    }

    public void setGroupId(int groupId) {
        this.groupId = groupId;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public int getGroupOrder() {
        return groupOrder;
    }

    public void setGroupOrder(int groupOrder) {
        this.groupOrder = groupOrder;
    }

    public String getGroupRegDate() {
        return groupRegDate;
    }

    public void setGroupRegDate(String groupRegDate) {
        this.groupRegDate = groupRegDate;
    }

    public String getGroupEditDate() {
        return groupEditDate;
    }

    public void setGroupEditDate(String groupEditDate) {
        this.groupEditDate = groupEditDate;
    }
}
